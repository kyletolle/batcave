def cmd_clear():
    """Wipe the Live note back to its header and delete every clip MP3.
    Only touches MP3_DIR (batspeaker/), so standalone test/comparison clips
    in the parent Attachments folder are left alone."""
    removed = 0
    for path in glob.glob(os.path.join(MP3_DIR, "*.mp3")):
        try:
            os.remove(path)
            removed += 1
        except Exception:
            pass
    write_note([], load_config()["keep"])
    jlog("clear", removed=removed)
    print(f"Cleared Bat-Speaker Live and deleted {removed} clip(s). Clean slate.")


